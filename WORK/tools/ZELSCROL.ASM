CODE_SEG_A	SEGMENT
		ASSUME	CS:CODE_SEG_A,DS:CODE_SEG_A,SS:CODE_SEG_A
		ORG	80H
LEN:		DB	?
  		ORG	100H
START:		JMP	M0
NAM:		DB	'ORIG',0
MESSAGE:	DB	0DH,0AH,'FILE ERROR !!!$'
HANDLE:		DW	?
M0:		XOR	BH,BH
		MOV	BL,BYTE PTR LEN
		MOV	[BX+81H],BH	;ASCII->ASCIIZ
		MOV	DX,82H
		MOV	AX,3D00H	;OPEN FILE @ DS:DX
		INT	21H
		JC	_ERR
		MOV	WORD PTR HANDLE,AX
		MOV	CX,900H
		MOV	BX,AX
		MOV	DX,OFFSET LAB
		MOV	AH,3FH
		INT	21H
		JC	_ERR
		MOV	BX,WORD PTR HANDLE
		MOV	AH,3EH		;CLOSE FILE
		INT	21H
		JNC	BEGIN
_ERR:		MOV	DX,OFFSET MESSAGE
		MOV	AH,9
		INT	21H
		RET
BEGIN:		MOV	DX,OFFSET NAM
		MOV	AX,3D00H
		INT	21H
		JC	_ERR
		MOV	WORD PTR HANDLE,AX
		MOV	CX,660H
		MOV	BX,AX
		MOV	DX,OFFSET LAB+900H
		MOV	AH,3FH
		INT	21H
		JC	_ERR
		MOV	BX,WORD PTR HANDLE
		MOV	AH,3EH		;CLOSE FILE
		INT	21H
		JC	_ERR
		MOV	AX,4
		INT	10H		;320x200,CGA,&B800
		MOV	AX,0B800H
		MOV	ES,AX		;ES=VIDEO SEGMENT
		CLD			;FORWARD
NEW0:		MOV	BX,OFFSET LAB
NEW:		PUSH	BX
		XOR	DI,DI
		MOV	DX,1924H
MAIN:		MOV	AL,[BX]
		XOR	AH,AH
		OR	AL,AL
		PUSH	DI
		JNZ	NONZERO
		MOV	CX,4
RPT0:		STOSW			;0,2,4,6,...STRINGS
		ADD	DI,2000H-2
		STOSW			;1,3,5,7,...STRINGS
		ADD	DI,-2000H+80-2
		LOOP	RPT0
		JMP	SHORT BOTH
NONZERO:	DEC	AX
		MOV	CX,4
		ROL	AX,CL
		ADD	AX,OFFSET LAB+900H
		MOV	SI,AX
RPT1:		MOVSW
		ADD	DI,2000H-2
		MOVSW
		ADD	DI,-2000H+80-2
		LOOP	RPT1
BOTH:		POP	DI
		INC	BX
		INC	DI
		INC	DI
		DEC	DL
		JNZ	MAIN
		CMP	BX,OFFSET LAB+900H
		JNZ	CONT1
		MOV	BX,OFFSET LAB
CONT1:		MOV	DL,24H
		ADD	DI,0F8H
		DEC	DH
		JNZ	MAIN
		POP	BX
SCAN:		XOR	AH,AH
		INT	16H
		CMP	AX,4800H	;UP
		JNZ	DOWN
		SUB	BX,24H
		CMP	BX,OFFSET LAB-24H
		JNZ	NEW
		MOV	BX,OFFSET LAB+900H-24H
		JMP	NEW
DOWN:		CMP	AX,5000H	;DOWN
		JNZ	ESC_
		ADD	BX,24H
		CMP	BX,OFFSET LAB+900H
		JNZ	NEW
		JMP	SHORT NEW0
ESC_:		CMP	AL,1BH		;ESC
		JNZ	SCAN
		MOV	AX,3
		INT	10H
		RET
LAB:
CODE_SEG_A	ENDS
		END	START